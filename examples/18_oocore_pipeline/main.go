// Example 18_oocore_pipeline — the full out-of-core (Tier 2) pipeline:
// generate a directed web-link graph as a CSV edge list, ingest it through
// the CSV reader, freeze it into a CSR snapshot, persist that snapshot as an
// on-disk csrfile, re-open the file by mmap, and run semi-external BFS plus
// PageRank directly over the mapped region.
//
// # What it demonstrates
//
// This is the complete five-stage out-of-core flow, end to end:
//
//	CSV bytes -> adjacency list -> CSR snapshot -> csrfile (disk) -> mmap -> {BFS, PageRank}
//
// Example 05 builds the adjacency directly in memory; this example extends it
// by routing the same scale-free graph through the CSV interchange stage
// first, so the whole ingest path is exercised. The CSV reader interns each
// page name to a compact NodeID and returns an in-memory adjacency list;
// [csr.BuildFromAdjList] freezes it into an immutable Tier 1 CSR snapshot;
// [csrfile.WriteToFile] persists that snapshot to disk in the Tier 2 binary
// format; and [csrfile.Open] re-attaches it by mmap. From that point the graph
// is queried straight out of the mapped pages: the semi-external
// [extern.BFSCtx] and [extern.PageRankCtx] keep only vertex-sized auxiliary
// state (a visited bitset, the rank vector) in RAM while streaming the
// adjacency from the mapped file. That is the headline out-of-core advantage
// this example measures: the on-disk adjacency dwarfs the resident heap.
//
// The csrfile preserves NodeIDs across the mmap boundary but drops the
// page-name -> NodeID mapping, so the BFS needs a known numeric seed. The
// example therefore captures the seed page's NodeID from the CSV reader's
// mapper before the adjacency (and its mapper) is discarded — exactly the
// pattern a real out-of-core consumer must follow.
//
// # Model
//
// The dataset is a directed, scale-free hyperlink graph generated by Price's
// model (de Solla Price, 1965/1976) — the directed preferential-attachment
// process that is the canonical domain for PageRank. Pages arrive in id order
// 0..N-1; each arriving page emits m citation out-links to distinct earlier
// pages, choosing each target with probability proportional to its current
// in-degree plus a small attractiveness constant a (in-degree exponent
// gamma_in = 2 + a/m). This produces a heavy-tailed in-degree distribution: a
// few early-arriving pages accrue most inbound links and become high-PageRank
// authorities, while the bulk hold near-uniform low rank. Because the
// authorities separate from the bulk by a wide margin, the top-k
// highest-PageRank node ids (tie-broken by ascending id) are a stable,
// meaningful fact.
//
// Pure citation edges always point from a newer page to an older one, which
// makes the graph a shallow DAG: forward BFS from an old page reaches almost
// nothing. To give the semi-external BFS a meaningful, stable reachable count,
// each arriving page also receives, with probability navFrac, one extra
// "navigation" link from a uniformly random earlier page (older -> newer).
// These forward links build long forward paths without disturbing the
// in-degree authority tail. The BFS is seeded from a fixed mid-graph "portal"
// page (id N/4, never the oldest page, which is a pure sink), from which a
// large but bounded fraction of the graph is reachable.
//
// Determinism. The whole graph is drawn from a single math/rand source seeded
// from cfg.seed, the build is single-threaded, edges are emitted in draw
// order, and the CSV is written in that same order — so the realised shape,
// the BFS reachable count, and the PageRank top-k are byte-identical across
// machines for a fixed -seed. Only the telemetry (lines prefixed "# ") varies
// between runs and machines.
//
// # Scale
//
// Run with no flags, the example builds a small deterministic default (4000
// pages, ~16k citation edges plus ~2k navigation edges) that completes in
// milliseconds — the size the regression test pins. The same binary scales up
// to where the out-of-core footprint advantage is observable:
//
//	go run ./examples/18_oocore_pipeline -nodes 2000000 -out-degree 8
//
// At two million pages and eight out-links each the on-disk csrfile is on the
// order of ~270 MiB while the resident rank vector is only ~16 MiB — a
// disk-over-heap advantage of roughly 17x that widens as the graph grows. The
// adjacency lives on disk, not in RAM.
