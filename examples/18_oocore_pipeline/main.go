// config captures every scale and shape knob of the pipeline. The zero value
// is not valid; build one with defaultConfig and override fields from flags
// (see main) or construct one directly (see the regression test).
type config struct {
	nodes      int     // number of pages in the generated web graph
	outDegree  int     // citation out-links emitted per arriving page (Price's m)
	attractive int64   // additive attractiveness constant (Price's a; >= 0)
	navFrac    float64 // probability an arriving page also gets one forward "navigation" link
	topK       int     // number of highest-PageRank nodes to report as facts
	seed       int64   // RNG seed; fixes the deterministic data shape
}

// defaultConfig returns the small, deterministic default the regression test
// pins: a 4000-page scale-free web graph with four citation out-links per page
// and a navigation link on half the pages, which builds, persists, mmaps, and
// queries in milliseconds yet still separates a clear top-k of authorities
// from the bulk and yields a stable, non-trivial BFS reachable count.
func defaultConfig() config {
	return config{
		nodes:      4000,
		outDegree:  4,
		attractive: 1,
		navFrac:    0.5,
		topK:       10,
		seed:       7,
	}
}

// validate rejects a configuration that cannot produce the requested shape. It
// is checked once, at the boundary, before any work. Price's model needs at
// least outDegree existing pages for the first arriving page to find distinct
// targets, so nodes must exceed outDegree; the attractiveness constant must be
// positive for the integer mixture draw to be well-defined; navFrac is a
// probability in [0,1]; and topK cannot exceed the node count.
func (c config) validate() error {
	switch {
	case c.nodes <= 0:
		return fmt.Errorf("nodes must be > 0, got %d", c.nodes)
	case c.outDegree <= 0:
		return fmt.Errorf("out-degree must be > 0, got %d", c.outDegree)
	case c.nodes <= c.outDegree:
		return fmt.Errorf("nodes (%d) must exceed out-degree (%d): not enough targets", c.nodes, c.outDegree)
	case c.attractive <= 0:
		return fmt.Errorf("attractiveness must be > 0, got %d", c.attractive)
	case c.navFrac < 0 || c.navFrac > 1:
		return fmt.Errorf("nav-frac must be within [0,1], got %g", c.navFrac)
	case c.topK <= 0:
		return fmt.Errorf("top-k must be > 0, got %d", c.topK)
	case c.topK > c.nodes:
		return fmt.Errorf("top-k (%d) exceeds nodes (%d)", c.topK, c.nodes)
	}
	return nil
}

func main() {
	cfg := defaultConfig()
	flag.IntVar(&cfg.nodes, "nodes", cfg.nodes, "number of pages in the web graph")
	flag.IntVar(&cfg.outDegree, "out-degree", cfg.outDegree, "citation out-links per arriving page (Price's m)")
	flag.Int64Var(&cfg.attractive, "attractiveness", cfg.attractive, "additive attractiveness constant (Price's a; > 0)")
	flag.Float64Var(&cfg.navFrac, "nav-frac", cfg.navFrac, "probability an arriving page also gets a forward navigation link")
	flag.IntVar(&cfg.topK, "top-k", cfg.topK, "number of highest-PageRank node ids to report")
	flag.Int64Var(&cfg.seed, "seed", cfg.seed, "RNG seed (fixes the deterministic data shape)")
	flag.Parse()

	if err := run(context.Background(), os.Stdout, cfg); err != nil {
		log.Fatal(err)
	}
}

// run drives the full out-of-core pipeline described by cfg and writes a
// report to w. It generates the scale-free web graph as a CSV edge list,
// ingests it through the CSV reader, freezes the adjacency into a CSR
// snapshot, persists that snapshot as a Tier 2 csrfile, re-opens the file via
// mmap, and runs semi-external BFS plus PageRank over the mapped region.
//
// Bare lines carry deterministic facts (counts, the BFS reachable count, and
// the PageRank top-k node ids — all reproducible for a fixed seed); lines
// prefixed with "# " carry volatile telemetry (per-stage wall-clock and the
// disk-vs-heap footprint) that varies per run and per machine. All output goes
// to w so a test can capture and assert on the deterministic lines. run
// returns wrapped errors rather than terminating the process and honours ctx
// cancellation.
//
// The csrfile is written under an os.MkdirTemp directory whose absolute path
// differs on every run, so run never prints that path; every fact line it
// emits is byte-stable.
func run(ctx context.Context, w io.Writer, cfg config) error {
	if err := cfg.validate(); err != nil {
		return fmt.Errorf("config: %w", err)
	}

	fmt.Fprintf(w, "config.nodes=%d\n", cfg.nodes)
	fmt.Fprintf(w, "config.out_degree=%d\n", cfg.outDegree)
	fmt.Fprintf(w, "config.attractiveness=%d\n", cfg.attractive)
	fmt.Fprintf(w, "config.nav_frac=%g\n", cfg.navFrac)
	fmt.Fprintf(w, "config.top_k=%d\n", cfg.topK)
	fmt.Fprintf(w, "config.seed=%d\n", cfg.seed)

	dir, err := os.MkdirTemp("", "gograph-ex18-")
	if err != nil {
		return fmt.Errorf("MkdirTemp: %w", err)
	}
	defer func() { _ = os.RemoveAll(dir) }()
	path := filepath.Join(dir, "graph.csr")

	// Stage 1 — generate the directed scale-free web graph and serialise it
	// to an in-memory CSV edge list. seedPage is the page name the BFS is
	// later seeded from; csvBytes is the size of the serialised CSV input.
	csvStart := time.Now()
	csvBuf, seedPage, genEdges := generateCSV(ctx, cfg)
	if err := ctx.Err(); err != nil {
		return err
	}
	csvBytes := uint64(csvBuf.Len()) //nolint:gosec // G115: a buffer length is non-negative

	// Stage 1 (cont.) — ingest the CSV through the edge-list reader, which
	// interns each page name to a compact NodeID and returns an in-memory
	// adjacency list.
	adj, nEdges, err := csv.ReadIntoCtx(ctx, csvBuf, csv.DefaultOptions())
	if err != nil {
		return fmt.Errorf("csv.ReadIntoCtx: %w", err)
	}
	csvElapsed := time.Since(csvStart)

	// Capture the seed page's NodeID before the adjacency (and its mapper) is
	// discarded: the csrfile preserves NodeIDs across the mmap boundary but
	// drops the name -> NodeID mapping, so the BFS below needs this numeric
	// seed.
	seedID, ok := adj.Mapper().Lookup(seedPage)
	if !ok {
		return fmt.Errorf("seed page %q not interned", seedPage)
	}

	// Stage 2 — freeze the adjacency into an immutable Tier 1 CSR snapshot.
	csrStart := time.Now()
	c := csr.BuildFromAdjList(adj)
	csrElapsed := time.Since(csrStart)

	fmt.Fprintf(w, "csv.edges=%d\n", nEdges)
	fmt.Fprintf(w, "csr.order=%d\n", c.Order())
	fmt.Fprintf(w, "csr.size=%d\n", c.Size())

	// Stage 3 — write the CSR to disk in the csrfile binary format. The write
	// is atomic (write .tmp, fsync, rename). Measure the resulting on-disk
	// size: this is the adjacency footprint that, at scale, dwarfs the
	// resident auxiliary state.
	if err := ctx.Err(); err != nil {
		return err
	}
	writeStart := time.Now()
	if _, err := csrfile.WriteToFile(path, c); err != nil {
		return fmt.Errorf("csrfile.WriteToFile: %w", err)
	}
	writeElapsed := time.Since(writeStart)
	diskBytes, err := fileSize(path)
	if err != nil {
		return err
	}

	// Stage 4 — re-open the file via mmap and hint SEQUENTIAL access so the OS
	// reads ahead while the queries stream the adjacency. The mmap call maps
	// the whole file into the address space without reading it into the heap;
	// time it to show that re-attaching an on-disk graph is near-instant.
	mmapStart := time.Now()
	r, err := csrfile.Open(path)
	if err != nil {
		return fmt.Errorf("csrfile.Open: %w", err)
	}
	defer func() { _ = r.Close() }()
	if err := r.SetHint(csrfile.AccessSequential); err != nil {
		return fmt.Errorf("SetHint: %w", err)
	}
	mmapElapsed := time.Since(mmapStart)

	// Stage 5a — semi-external BFS from the portal page. Only the visited
	// bitset and frontier live in RAM; the adjacency streams from the mapped
	// file. BFSCtx honours ctx cancellation once per BFS level.
	bfsStart := time.Now()
	reached := 0
	if err := extern.BFSCtx(ctx, r, seedID, func(_ graph.NodeID, _ int) bool {
		reached++
		return true
	}); err != nil {
		return fmt.Errorf("extern.BFSCtx: %w", err)
	}
	bfsElapsed := time.Since(bfsStart)

	// Stage 5b — semi-external PageRank over the same mapped region. Only the
	// rank vector lives in RAM; the adjacency streams from the file each
	// iteration. PageRankCtx honours ctx cancellation at every iteration.
	prStart := time.Now()
	ranks, iters, err := extern.PageRankCtx(ctx, r, extern.DefaultPageRankOptions())
	if err != nil {
		return fmt.Errorf("extern.PageRankCtx: %w", err)
	}
	prElapsed := time.Since(prStart)

	// Resident heap after the computation, forcing a GC first so HeapAlloc
	// reflects live bytes. The rank vector is the dominant query-time
	// allocation, so its size is the in-RAM working set we contrast with the
	// on-disk adjacency.
	mem := readMem()
	rankVecBytes := uint64(len(ranks)) * 8 //nolint:gosec // G115: len is non-negative

	// Deterministic results. The BFS reachable count and the PageRank top-k
	// node ids are stable for a fixed seed; the top-k ids separate the
	// authorities from the bulk by a wide margin and ties are broken by
	// ascending id (see topPageRank).
	fmt.Fprintf(w, "bfs.seed_node=%d\n", seedID)
	fmt.Fprintf(w, "bfs.reachable=%d\n", reached)

	top := topPageRank(ranks, cfg.topK)
	if len(top) == 0 {
		return fmt.Errorf("PageRank produced no live ranks")
	}
	for i, id := range top {
		fmt.Fprintf(w, "pagerank.top%d=%d\n", i, id)
	}

	fmt.Fprintf(w, "# gen.edges=%d\n", genEdges)
	fmt.Fprintf(w, "# stage.csv_ingest=%s\n", csvElapsed.Round(time.Microsecond))
	fmt.Fprintf(w, "# stage.csr_build=%s\n", csrElapsed.Round(time.Microsecond))
	fmt.Fprintf(w, "# stage.csrfile_write=%s\n", writeElapsed.Round(time.Microsecond))
	fmt.Fprintf(w, "# stage.mmap_open=%s\n", mmapElapsed.Round(time.Microsecond))
	fmt.Fprintf(w, "# stage.bfs=%s\n", bfsElapsed.Round(time.Microsecond))
	fmt.Fprintf(w, "# stage.pagerank=%s\n", prElapsed.Round(time.Microsecond))
	fmt.Fprintf(w, "# disk.csv_input=%s\n", humanBytes(csvBytes))
	fmt.Fprintf(w, "# disk.csrfile=%s\n", humanBytes(diskBytes))
	fmt.Fprintf(w, "# mem.rank_vector=%s\n", humanBytes(rankVecBytes))
	fmt.Fprintf(w, "# mem.heap_alloc=%s\n", humanBytes(mem.HeapAlloc))
	fmt.Fprintf(w, "# ooc.disk_over_rank_vector=%.1fx\n", safeDiv(float64(diskBytes), float64(rankVecBytes)))
	fmt.Fprintf(w, "# pagerank.iterations=%d\n", iters)
	return nil
}

// generateCSV builds the directed scale-free web graph described by cfg using
// Price's model (directed preferential attachment) augmented with forward
// navigation links, and serialises it to an in-memory CSV edge list
// ("src,dst,1" per line). It returns the CSV buffer, the page name the BFS
// should be seeded from (id N/4, the mid-graph "portal"), and the number of
// edges written.
//
// Pages arrive in id order. A fully-wired seed core of outDegree pages is laid
// down first so the early target draws are well-defined; then each subsequent
// page v emits outDegree citation out-links to distinct earlier pages, each
// target chosen with probability proportional to its current in-degree plus
// the attractiveness constant. With probability navFrac, page v additionally
// receives one navigation in-link from a uniformly random earlier page, giving
// the forward reachability the BFS measures.
//
// Determinism. The whole graph is drawn from a single math/rand source seeded
// from cfg.seed, the build is single-threaded, and every edge is written to
// the CSV in the order it was drawn (never iterated from a map, whose order is
// randomised in Go) — so the serialised CSV, and hence every fact the pipeline
// reports, is byte-identical across machines for a fixed seed.
//
// Complexity is O(nodes * outDegree) = O(edges). The edge-copying target list
// copyTargets makes in-degree-proportional selection O(1) per pick: each
// citation edge's destination appears in it once, so a uniform draw from it is
// automatically proportional to in-degree.
func generateCSV(ctx context.Context, cfg config) (*bytes.Buffer, string, int) {
	//nolint:gosec // G404: a seeded math/rand is intentional here — the example
	// must reproduce a fixed dataset for a given -seed; crypto/rand would defeat that.
	rng := rand.New(rand.NewSource(cfg.seed))

	var buf bytes.Buffer
	edges := 0
	emit := func(src, dst int) {
		buf.WriteString(pageName(src))
		buf.WriteByte(',')
		buf.WriteString(pageName(dst))
		buf.WriteString(",1\n")
		edges++
	}

	m := cfg.outDegree
	// copyTargets holds one entry per existing citation edge: the edge's
	// destination. A page therefore appears in it exactly citation-in-degree
	// times, so a uniform draw is in-degree-proportional.
	copyTargets := make([]int, 0, cfg.nodes*m)

	// Seed core: the first m pages, wired so that page v links to every
	// earlier page u < v. This bootstraps copyTargets so the preferential
	// draws below always have a non-empty in-degree pool to sample.
	for v := 1; v < m; v++ {
		for u := 0; u < v; u++ {
			emit(v, u)
			copyTargets = append(copyTargets, u)
		}
	}

	targets := make([]int, 0, m)
	seen := make(map[int]struct{}, m)
	for v := m; v < cfg.nodes; v++ {
		if v%checkEvery == 0 {
			if err := ctx.Err(); err != nil {
				return &buf, pageName(cfg.nodes / 4), edges
			}
		}
		targets = targets[:0]
		clear(seen)
		// Draw m distinct citation targets. The mixture weight at v's arrival
		// is attractive*v (one unit of attractiveness per existing page 0..v-1)
		// plus len(copyTargets) (one per existing citation edge). A single
		// integer draw over that total picks either the uniform-page branch or
		// the in-degree-proportional copy branch, with no floating-point bias.
		for len(targets) < m {
			total := cfg.attractive*int64(v) + int64(len(copyTargets))
			r := rng.Int63n(total)
			var t int
			if r < cfg.attractive*int64(v) {
				t = int(r / cfg.attractive)
			} else {
				t = copyTargets[r-cfg.attractive*int64(v)]
			}
			if t == v {
				continue // belt-and-braces: t < v always holds here
			}
			if _, dup := seen[t]; dup {
				continue // distinct citation targets per arriving page
			}
			seen[t] = struct{}{}
			targets = append(targets, t)
		}
		// Emit citations in draw order (fixed), updating copyTargets so later
		// pages see the new in-edges.
		for _, t := range targets {
			emit(v, t)
			copyTargets = append(copyTargets, t)
		}
		// Navigation link: with probability navFrac, a uniformly random earlier
		// page links forward to v. This injects forward paths (older -> newer)
		// so the BFS reaches a large but bounded fraction of the graph. The
		// draw is taken unconditionally (even when it will not be used) so that
		// adding or removing navigation never shifts the citation RNG stream.
		s := rng.Intn(v)
		if rng.Float64() < cfg.navFrac {
			emit(s, v)
		}
	}
	return &buf, pageName(cfg.nodes / 4), edges
}

// checkEvery bounds how often generateCSV polls ctx for cancellation: often
// enough that a cancelled multi-million-page build stops promptly, rare enough
// that the check is free relative to the surrounding work.
const checkEvery = 4096

// pageName formats a page id as a stable, zero-padded "page-NNNNNN" name. The
// fixed width keeps the CSV columns aligned and the names sortable, and the
// formatting is pure (no RNG), so it never disturbs the deterministic shape.
func pageName(id int) string {
	return "page-" + strconv.Itoa(id)
}

// topPageRank returns the ids of the k highest-PageRank live nodes, in
// descending rank order, ties broken by ascending node id. The rank slice is
// indexed by NodeID and its length is the sharded, MaxNodeID-rounded vertex
// count, so it carries ghost slots (zero rank) beyond the live nodes; those
// are skipped. The ascending-id tie-break is total and machine-independent, so
// the result is stable for a fixed seed.
func topPageRank(ranks []float64, k int) []graph.NodeID {
	type nodeRank struct {
		id   graph.NodeID
		rank float64
	}
	live := make([]nodeRank, 0, len(ranks))
	for i, x := range ranks {
		if x == 0 {
			continue
		}
		live = append(live, nodeRank{id: graph.NodeID(i), rank: x}) //nolint:gosec // G115: index is non-negative
	}
	sort.Slice(live, func(i, j int) bool {
		if live[i].rank != live[j].rank {
			return live[i].rank > live[j].rank
		}
		return live[i].id < live[j].id
	})
	if k > len(live) {
		k = len(live)
	}
	out := make([]graph.NodeID, k)
	for i := 0; i < k; i++ {
		out[i] = live[i].id
	}
	return out
}

