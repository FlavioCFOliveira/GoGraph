// Node labels. Centralised so the model is described in exactly one place
// and a rename surfaces as a compile error everywhere it is used.
const (
	labelPackage    = "Package"
	labelDeprecated = "Deprecated"
)

// Node property keys. Every Package carries all four.
const (
	propID        = "id"        // 16-char hex string, the unique key
	propName      = "name"      // realistic package name (may repeat)
	propEcosystem = "ecosystem" // categorical: go/npm/pypi/cargo/maven
	propLicense   = "license"   // categorical: MIT/Apache-2.0/...
	propDownloads = "downloads" // int64 popularity proxy
)

// matchEcosystem and matchLicense are the categorical values the property
// predicates select on. They are members of the fixed sets below, so the
// label+property queries always match a deterministic, non-empty subset.
const (
	matchEcosystem = "go"
	matchLicense   = "MIT"
)

// config captures every scale and shape knob. The zero value is not
// valid; build one with defaultConfig and override fields from flags (see
// main) or construct one directly (see the regression test).
type config struct {
	nodes        int   // number of :Package nodes
	outDegreeMin int   // minimum direct-dependency count per package (inclusive)
	outDegreeMax int   // maximum direct-dependency count per package (inclusive)
	deprecatedN  int   // 1-in-deprecatedN packages also carry the Deprecated label
	seed         int64 // RNG seed; fixes the deterministic data shape
}

// defaultConfig returns the small, deterministic default the regression
// test pins. It is large enough that the preferential-attachment topology
// and the label/property selectivity are exercised, yet small enough to
// build and query well under the short-layer 60 s package budget.
func defaultConfig() config {
	return config{
		nodes:        2000,
		outDegreeMin: 2,
		outDegreeMax: 6,
		deprecatedN:  8,
		seed:         1,
	}
}

// validate rejects a configuration that cannot produce the requested
// shape. It is checked once, at the boundary, before any work. An edge
// from package i may only target a lower-indexed package, so a package's
// out-degree is capped at its index; the generator clamps each draw to
// min(outDegree, i), which is why outDegreeMax need not be bounded by
// nodes here — only the basic range and positivity invariants are.
func (c config) validate() error {
	switch {
	case c.nodes <= 0:
		return fmt.Errorf("nodes must be > 0, got %d", c.nodes)
	case c.outDegreeMin < 0 || c.outDegreeMax < c.outDegreeMin:
		return fmt.Errorf("require 0 <= outDegreeMin <= outDegreeMax, got [%d,%d]", c.outDegreeMin, c.outDegreeMax)
	case c.deprecatedN <= 0:
		return fmt.Errorf("deprecatedN must be > 0, got %d", c.deprecatedN)
	}
	return nil
}

func main() {
	cfg := defaultConfig()
	flag.IntVar(&cfg.nodes, "nodes", cfg.nodes, "number of Package nodes")
	flag.IntVar(&cfg.outDegreeMin, "out-min", cfg.outDegreeMin, "minimum direct-dependency count per package")
	flag.IntVar(&cfg.outDegreeMax, "out-max", cfg.outDegreeMax, "maximum direct-dependency count per package")
	flag.IntVar(&cfg.deprecatedN, "deprecated-n", cfg.deprecatedN, "1-in-N packages are also Deprecated")
	flag.Int64Var(&cfg.seed, "seed", cfg.seed, "RNG seed (fixes the deterministic data shape)")
	flag.Parse()

	if err := run(context.Background(), os.Stdout, cfg); err != nil {
		log.Fatal(err)
	}
}

// run builds the dependency graph described by cfg, freezes it into a CSR
// snapshot, runs the fluent pattern-query battery, and writes a report to
// w. Bare lines carry deterministic facts (counts and read-back property
// values, reproducible for a fixed seed); lines prefixed with "# " carry
// volatile telemetry (durations and heap figures) that vary per run and
// per machine. All output goes to w so a test can capture and assert on
// the deterministic lines.
func run(ctx context.Context, w io.Writer, cfg config) error {
	if err := cfg.validate(); err != nil {
		return fmt.Errorf("config: %w", err)
	}

	fmt.Fprintf(w, "config.nodes=%d\n", cfg.nodes)
	fmt.Fprintf(w, "config.out_degree=[%d,%d]\n", cfg.outDegreeMin, cfg.outDegreeMax)
	fmt.Fprintf(w, "config.deprecated_n=%d\n", cfg.deprecatedN)
	fmt.Fprintf(w, "config.seed=%d\n", cfg.seed)

	base := readMem()

	g := lpg.New[string, int64](adjlist.Config{Directed: true})
	declareSchema(g)

	stats, err := build(ctx, g, cfg)
	if err != nil {
		return fmt.Errorf("build: %w", err)
	}

	// This is a build-then-query workload: the graph is fully assembled
	// above and only read from here on. Compact right-sizes the adjacency
	// backing arrays so the resident-heap figures below reflect the tight
	// arrays the query phase runs against.
	if err := ctx.Err(); err != nil {
		return err
	}
	g.AdjList().Compact(ctx)

	fmt.Fprintf(w, "nodes.packages=%d\n", stats.nodes)
	fmt.Fprintf(w, "nodes.deprecated=%d\n", stats.deprecated)
	fmt.Fprintf(w, "edges.depends_on=%d\n", stats.edges)

	built := readMem()
	fmt.Fprintf(w, "# build.elapsed=%s\n", stats.elapsed.Round(time.Millisecond))
	fmt.Fprintf(w, "# build.node_rate=%.0f nodes/s\n", rate(stats.nodes, stats.elapsed))
	fmt.Fprintf(w, "# build.edge_rate=%.0f edges/s\n", rate(stats.edges, stats.elapsed))
	fmt.Fprintf(w, "# mem.heap_alloc=%s\n", humanBytes(built.HeapAlloc))
	fmt.Fprintf(w, "# mem.heap_growth=%s\n", humanBytes(built.HeapAlloc-base.HeapAlloc))

	// Freeze the mutable builder into an immutable CSR snapshot — the
	// adjacency surface the fluent query engine traverses for one-hop
	// expansion — and report how long the freeze took.
	freezeStart := time.Now()
	c := csr.BuildFromAdjList(g.AdjList())
	freeze := time.Since(freezeStart)
	eng := query.New(g, c)
	fmt.Fprintf(w, "# csr.freeze=%s\n", freeze.Round(time.Microsecond))

	return runQueries(ctx, eng, g, w)
}

// buildStats reports the realised shape of a build (the random out-degrees
// mean the edge total is not known until the graph is materialised) plus
// the wall-clock cost.
type buildStats struct {
	nodes      int
	deprecated int
	edges      int
	elapsed    time.Duration
}

// declareSchema registers the Package/Deprecated labels and the four
// typed property keys on g. The schema is declarative documentation of
// the model's shape here (it is not installed as a write-path validator),
// matching the original example's use of the schema package.
func declareSchema(g *lpg.Graph[string, int64]) {
	s := schema.New(g.Registry(), g.PropertyKeys())
	s.RegisterLabel(labelPackage)
	s.RegisterLabel(labelDeprecated)
	// RegisterProperty only fails on a kind redeclaration, which cannot
	// happen here because each key is registered exactly once with a fixed
	// kind; the values are discarded deliberately.
	_, _ = s.RegisterProperty(propID, lpg.PropString)
	_, _ = s.RegisterProperty(propName, lpg.PropString)
	_, _ = s.RegisterProperty(propEcosystem, lpg.PropString)
	_, _ = s.RegisterProperty(propLicense, lpg.PropString)
	_, _ = s.RegisterProperty(propDownloads, lpg.PropInt64)
}

// build materialises the dependency graph described by cfg into g using
// Price's model. It first creates every package node (so DEPENDS_ON
// targets exist before edges reference them), recording each node's key,
// then adds the dependency edges from higher to lower index with
// preferential attachment by current in-degree. The build honours ctx
// cancellation on a periodic check.
func build(ctx context.Context, g *lpg.Graph[string, int64], cfg config) (buildStats, error) {
	//nolint:gosec // G404: a seeded math/rand is intentional here — the example
	// must reproduce a fixed dataset for a given -seed; crypto/rand would defeat that.
	rng := rand.New(rand.NewSource(cfg.seed))
	start := time.Now()

	keys := make([]string, cfg.nodes)
	seen := make(map[string]struct{}, cfg.nodes)

	// targets is the preferential-attachment urn: each existing node
	// appears once per incident in-edge plus once for its base weight, so
	// drawing a uniform element selects a target with probability
	// proportional to (in-degree + 1) in amortised O(1) per draw. It is
	// pre-sized to its exact final length: every node contributes one base
	// entry, and every edge contributes one in-degree entry.
	maxEdges := cfg.nodes * cfg.outDegreeMax
	targets := make([]int, 0, cfg.nodes+maxEdges)

	deprecated := 0
	edges := 0
	picked := make(map[int]struct{}, cfg.outDegreeMax)

	for i := 0; i < cfg.nodes; i++ {
		if i%checkEvery == 0 {
			if err := ctx.Err(); err != nil {
				return buildStats{}, err
			}
		}

		id := uniqueHexID(rng, seen)
		keys[i] = id
		isDeprecated := i%cfg.deprecatedN == 0
		if isDeprecated {
			deprecated++
		}
		if err := addPackage(g, id, rng, isDeprecated); err != nil {
			return buildStats{}, err
		}

		// Out-edges from i target only lower indices (keeps the graph an
		// acyclic dependency DAG), capped at the number of earlier nodes.
		degree := cfg.outDegreeMin + rng.Intn(cfg.outDegreeMax-cfg.outDegreeMin+1)
		if degree > i {
			degree = i
		}
		clear(picked)
		for len(picked) < degree {
			j := targets[rng.Intn(len(targets))]
			if _, dup := picked[j]; dup {
				continue
			}
			picked[j] = struct{}{}
			if err := g.AddEdge(id, keys[j], 1); err != nil {
				return buildStats{}, fmt.Errorf("AddEdge %s->%s: %w", id, keys[j], err)
			}
			edges++
			targets = append(targets, j) // j gained one in-edge
		}

		targets = append(targets, i) // i's base weight (in-degree + 1)
	}

	return buildStats{
		nodes:      cfg.nodes,
		deprecated: deprecated,
		edges:      edges,
		elapsed:    time.Since(start),
	}, nil
}

// checkEvery bounds how often the build polls ctx for cancellation: often
// enough that a cancelled large build stops promptly, rare enough that the
// check is free relative to the surrounding work.
const checkEvery = 4096

// addPackage adds one Package node carrying its four typed properties and,
// when deprecated, the additional Deprecated label. The property values
// are drawn from the seeded rng so the whole dataset is reproducible.
func addPackage(g *lpg.Graph[string, int64], id string, rng *rand.Rand, deprecated bool) error {
	if err := g.SetNodeLabel(id, labelPackage); err != nil {
		return fmt.Errorf("SetNodeLabel %s %s: %w", labelPackage, id, err)
	}
	if deprecated {
		if err := g.SetNodeLabel(id, labelDeprecated); err != nil {
			return fmt.Errorf("SetNodeLabel %s %s: %w", labelDeprecated, id, err)
		}
	}
	props := []struct {
		key string
		val lpg.PropertyValue
	}{
		{propID, lpg.StringValue(id)},
		{propName, lpg.StringValue(realisticName(rng))},
		{propEcosystem, lpg.StringValue(ecosystems[rng.Intn(len(ecosystems))])},
		{propLicense, lpg.StringValue(licenses[rng.Intn(len(licenses))])},
		// Downloads: a heavy-tailed popularity proxy in [0, ~10M).
		{propDownloads, lpg.Int64Value(int64(rng.Intn(10_000_000)))},
	}
	for _, p := range props {
		if err := g.SetNodeProperty(id, p.key, p.val); err != nil {
			return fmt.Errorf("SetNodeProperty %s %s: %w", p.key, id, err)
		}
	}
	return nil
}

// uniqueHexID returns a 16-character lowercase hex id (8 random bytes)
// that has not been handed out before, recording it in seen. The bytes
// come from the seeded rng (via rng.Read) so the dataset is reproducible
// and no integer-narrowing conversion is performed.
func uniqueHexID(rng *rand.Rand, seen map[string]struct{}) string {
	var b [8]byte
	for {
		_, _ = rng.Read(b[:]) // *rand.Rand.Read never returns an error
		id := hex.EncodeToString(b[:])
		if _, dup := seen[id]; dup {
			continue
		}
		seen[id] = struct{}{}
		return id
	}
}

// realisticName assembles a plausible package name of the form
// "<prefix>-<suffix>" from fixed word lists. Names are intentionally
// allowed to repeat — the unique key is the hex id, not the name.
func realisticName(rng *rand.Rand) string {
	return namePrefixes[rng.Intn(len(namePrefixes))] + "-" + nameSuffixes[rng.Intn(len(nameSuffixes))]
}

