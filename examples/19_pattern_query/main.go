// runQueries executes the fluent pattern-query suite against eng, printing
// one deterministic count line and one volatile latency line ("# ...") per
// query, then a small set of deterministic read-back property facts.
func runQueries(ctx context.Context, eng *query.Engine[string, int64], g *lpg.Graph[string, int64], w io.Writer) error {
	// Label-only patterns: a single label scan, and two labels intersected.
	counts := []struct {
		name    string
		pattern func() *query.Pattern[string, int64]
	}{
		{"all_packages", func() *query.Pattern[string, int64] {
			return eng.Match().Vertex(query.WithLabel[string, int64](labelPackage))
		}},
		{"deprecated", func() *query.Pattern[string, int64] {
			return eng.Match().Vertex(
				query.WithLabel[string, int64](labelPackage),
				query.WithLabel[string, int64](labelDeprecated),
			)
		}},
		// Label + property-equality predicate: an ecosystem, then a license.
		{"ecosystem_go", func() *query.Pattern[string, int64] {
			return eng.Match().Vertex(
				query.WithLabel[string, int64](labelPackage),
				query.WithProperty[string, int64](propEcosystem, lpg.StringValue(matchEcosystem)),
			)
		}},
		{"license_mit", func() *query.Pattern[string, int64] {
			return eng.Match().Vertex(
				query.WithLabel[string, int64](labelPackage),
				query.WithProperty[string, int64](propLicense, lpg.StringValue(matchLicense)),
			)
		}},
	}
	for _, q := range counts {
		if err := ctx.Err(); err != nil {
			return err
		}
		start := time.Now()
		n := q.pattern().Cardinality()
		d := time.Since(start)
		fmt.Fprintf(w, "q.%s=%d\n", q.name, n)
		fmt.Fprintf(w, "# q.%s.latency=%s\n", q.name, d.Round(time.Microsecond))
	}

	// One-hop expansion: the distinct direct dependencies of every
	// deprecated package — (:Package:Deprecated)-->(b). This is the
	// capability the original toy demonstrated, now over the generated DAG.
	if err := ctx.Err(); err != nil {
		return err
	}
	start := time.Now()
	deps := eng.Match().
		Vertex(
			query.WithLabel[string, int64](labelPackage),
			query.WithLabel[string, int64](labelDeprecated),
		).
		Out().
		Cardinality()
	d := time.Since(start)
	fmt.Fprintf(w, "q.deprecated_deps=%d\n", deps)
	fmt.Fprintf(w, "# q.deprecated_deps.latency=%s\n", d.Round(time.Microsecond))

	return readBack(eng, g, w)
}

// readBack reads matched properties back out for the top packages by
// download count, demonstrating that a matched node's typed properties
// can be retrieved after a pattern query. The ordering is a total order
// (downloads DESC, id ASC) so the reported values are byte-stable for a
// fixed seed. Exactly readBackTop rows are printed.
func readBack(eng *query.Engine[string, int64], g *lpg.Graph[string, int64], w io.Writer) error {
	matched := eng.Match().Vertex(
		query.WithLabel[string, int64](labelPackage),
		query.WithProperty[string, int64](propEcosystem, lpg.StringValue(matchEcosystem)),
	).Collect()

	type row struct {
		id        string
		downloads int64
		license   string
	}
	rows := make([]row, 0, len(matched))
	for _, key := range matched {
		dl, ok := g.GetNodeProperty(key, propDownloads)
		if !ok {
			return fmt.Errorf("package %q missing property %s", key, propDownloads)
		}
		dlv, ok := dl.Int64()
		if !ok {
			return fmt.Errorf("package %q property %s is not an int64", key, propDownloads)
		}
		lic, ok := g.GetNodeProperty(key, propLicense)
		if !ok {
			return fmt.Errorf("package %q missing property %s", key, propLicense)
		}
		licv, ok := lic.String()
		if !ok {
			return fmt.Errorf("package %q property %s is not a string", key, propLicense)
		}
		rows = append(rows, row{id: key, downloads: dlv, license: licv})
	}

	// Total order: highest downloads first, id ascending as the tiebreak,
	// so the read-back is deterministic regardless of match iteration order.
	sort.Slice(rows, func(a, b int) bool {
		if rows[a].downloads != rows[b].downloads {
			return rows[a].downloads > rows[b].downloads
		}
		return rows[a].id < rows[b].id
	})

	fmt.Fprintf(w, "readback.rows=%d\n", min(readBackTop, len(rows)))
	for i := 0; i < readBackTop && i < len(rows); i++ {
		r := rows[i]
		fmt.Fprintf(w, "readback.%d=id:%s downloads:%d license:%s\n", i, r.id, r.downloads, r.license)
	}
	return nil
}

// readBackTop is how many top-by-downloads matched packages the read-back
// phase prints. Small and fixed so the deterministic fact lines stay
// compact and a regression test can pin them.
const readBackTop = 3

